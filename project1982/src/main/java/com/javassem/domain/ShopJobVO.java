package com.javassem.domain;

public class ShopJobVO {
	private int shopNum;
	private String jobDate;
	private String jobTime;
	private String jobPay;
	private String jobEm;
	private String jobToday;
	private int jobNum;
	private String jobType;
	private String jobAddr;
	
	public int getShopNum() {
		return shopNum;
	}
	public void setShopNum(int shopNum) {
		this.shopNum = shopNum;
	}
	public String getJobDate() {
		return jobDate;
	}
	public void setJobDate(String jobDate) {
		this.jobDate = jobDate;
	}
	public String getJobTime() {
		return jobTime;
	}
	public void setJobTime(String jobTime) {
		this.jobTime = jobTime;
	}
	public String getJobPay() {
		return jobPay;
	}
	public void setJobPay(String jobPay) {
		this.jobPay = jobPay;
	}
	public String getJobEm() {
		return jobEm;
	}
	public void setJobEm(String jobEm) {
		this.jobEm = jobEm;
	}
	public String getJobToday() {
		return jobToday;
	}
	public void setJobToday(String jobToday) {
		this.jobToday = jobToday;
	}
	public int getJobNum() {
		return jobNum;
	}
	public void setJobNum(int jobNum) {
		this.jobNum = jobNum;
	}
	public String getJobType() {
		return jobType;
	}
	public void setJobType(String jobType) {
		this.jobType = jobType;
	}
	public String getJobAddr() {
		return jobAddr;
	}
	public void setJobAddr(String jobAddr) {
		this.jobAddr = jobAddr;
	}
}
